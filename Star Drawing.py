# This program creates a star

import turtle
windows = turtle.Screen()

alexTurtle = turtle.Turtle()

alexTurtle.pencolor("Black")
alexTurtle.fillcolor("Yellow")

alexTurtle.begin_fill()

for i in range(5):
    alexTurtle.forward(100)
    alexTurtle.right(144)

alexTurtle.end_fill()
